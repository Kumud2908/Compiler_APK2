#include <stdio.h>


int main() {
  
    /* for loop */
    for (int i=0; i<3; i++)
        printf("for i=%d\n", i);


    /* while loop */
    int j = 0;
    while (j < 3) {
        printf("while j=%d\n", j);
        j++;
    }

    for(int i=0;i<4;i++) printf("h");
    
    
}